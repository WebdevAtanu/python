a, b = 10, 3

# arithmetic operators
print(a + b)   # addition
print(a - b)   # subtraction
print(a * b)   # multiplication
print(a / b)   # division
print(a // b)  # floor division
print(a % b)   # modulus
print(a ** b)  # power

# assignment operators
a += b         # increment a by b
a -= b         # decrement a by b
a *= b         # multiply a by b
a /= b         # divide a by b
a //= b        # floor divide a by b
a %= b         # modulus a by b
a **= b        # power a by b

# comparison operators
print(a == b)  # equal to
print(a != b)  # not equal to
print(a > b)   # greater than
print(a < b)   # less than
print(a >= b)  # greater than or equal to
print(a <= b)  # less than or equal to

# logical operators
print(a and b) # and
print(a or b)  # or
print(not a)   # not

# bitwise operators
print(a & b)   # and
print(a | b)   # or
print(a ^ b)   # xor
print(~a)      # not
print(a << b)  # left shift
print(a >> b)  # right shift

# identity operators
print(a is b)   # is
print(a is not b)   # is not

# membership operators
print(a in b)   # in
print(a not in b)   # not in

# special operators
print(a is None)    # is None
print(a is not None)    # is not None

